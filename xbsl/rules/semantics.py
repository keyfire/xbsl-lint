"""Tier D: semantic checks against the stdlib catalog.

The code/unknown-type rule: the base type a piece of code references must be known – it is
either a stdlib symbol (xbsl/data/stdlib.json, generated by tools/extract_stdlib.py), or
a project object (the Имя of a yaml with a ВидЭлемента), or a locally declared type
(структура/перечисление/исключение). Every type position of the module is checked:

- `новый <Type>` – construction;
- `<expression> как <Type>` – a cast;
- the annotation of a declaration/field/catch `знч/пер/поймать <name>: <Type>` (including a
  comma-separated list of names with a shared type – `знч a, b: <Type>`);
- a method signature – the parameter types `(<name>: <Type>, ...)` and the return type `): <Type>`.

A type expression is parsed by its root: for an FQN (`КэшПрайса.СтрокаДанных`) only the
first segment is checked (nested types are not described by the catalog – we leave them alone
to avoid false positives); the nullable suffix `?` is ignored; for a generic
(`Массив<СотрудникJSON>`) both the base and each argument are checked. The namespace roots
(Справочник/Документ/...) are present in the catalog, so an FQN like `Справочник.Товары.Ссылка`
resolves by its root.

The rule is project-wide (cross-file): it is correct only with the whole project known (the
objects and local types of all modules), so in single-file mode (lint_source) it does not run
– this rules out false positives. Verified on the real corpus: 0 false positives.

The code/unknown-object-type rule goes one segment deeper: when the FQN root is a project
object, the second segment must belong to the family of types generated by that object –
a built-in derived type ("Типы, порождаемые элементом проекта" in the platform docs: Ссылка,
Объект, Данные, Запись, НаборЗаписей, the keys, the write/delete parameters...), a tabular
section from the object's yaml, or a structure/enumeration/exception declared in the object's
modules (`<Имя>.xbsl`, `<Имя>.<Часть>.xbsl`). The built-in list is a single union across the
object kinds rather than a per-kind table: a suffix valid for another kind passes (a false
negative), which keeps the rule free of false positives. Objects of kinds outside the checked
set are skipped entirely, as are namespace-qualified references (`Справочник.Имя.Ссылка`) –
the real corpus does not use this form, and objects supplied by external libraries would make
the check guess. Deeper segments (`НаборЗаписей.Данные`, nested report parameters) are not
checked.
"""

